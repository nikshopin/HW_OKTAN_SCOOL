let day = Math.floor(Math.random() * 7) + 1;

switch (day) {
    case 1:
        console.log(`${day} Понедельник встреча с семьей`);
        break;
    case 2:
        console.log(`${day} Вторник рабочий день`);
        break;
    case 3:
        console.log(`${day} Среда день спорта`);
        break;
    case 4:
        console.log(`${day} Четверг рыбный день(рыбалка)`);
        break;
    case 5:
        console.log(`${day} Пятница поездка по грибы`);
        break;
    case 6:
        console.log(`${day} Суббота день занятий it`);
        break;
    case 7:
        console.log(`${day} Воскресенье церковный день`);
        break;
}